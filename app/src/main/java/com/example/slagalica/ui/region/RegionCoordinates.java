    private static boolean pointInPolygon(double lat, double lng, double[][] polygon) {
        boolean inside = false;
        int n = polygon.length;
        for (int i = 0, j = n - 1; i < n; j = i++) {
            double xi = polygon[i][0], yi = polygon[i][1]; // lng, lat
            double xj = polygon[j][0], yj = polygon[j][1];
            boolean intersect = ((yi > lat) != (yj > lat))
                    && (lng < (xj - xi) * (lat - yi) / (yj - yi) + xi);
            if (intersect) inside = !inside;
        }
        return inside;
    }

    public static double[] getRandomPointInRegion(String region) {
        double[][] polygon = POLYGONS.get(region);
        double[] bounds = BOUNDS.get(region);
        if (polygon == null || bounds == null) return null;

        double minLat = bounds[0], maxLat = bounds[1];
        double minLng = bounds[2], maxLng = bounds[3];

        for (int attempt = 0; attempt < 100; attempt++) {
            double lat = minLat + RANDOM.nextDouble() * (maxLat - minLat);
            double lng = minLng + RANDOM.nextDouble() * (maxLng - minLng);
            if (pointInPolygon(lat, lng, polygon)) {
                return new double[]{lat, lng};
            }
        }

        return new double[]{
                (minLat + maxLat) / 2.0,
                (minLng + maxLng) / 2.0
        };
    }

